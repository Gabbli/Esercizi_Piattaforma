package TipiPrimitivi;
/*
Dichiarare 2 caratteri e stampare come risultato la
 combinazione dei due caratteri, affinché siano una stringa
 priva di spazi.
 */
public class TipiPrimitivi2 {
    public static void main(String[] args) {

        char primo = 'A';
        char secondo = 'B';
        String combinazione = "" + primo + secondo;

        System.out.println("La combinazione è: "+ combinazione);
    }
}
